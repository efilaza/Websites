<?php
/**
 * delete_action.php
 * Script για την ασφαλή διαγραφή μιας περιβαλλοντικής δράσης.
 */

// Εισαγωγή της σύνδεσης με τη βάση δεδομένων
include('db_connect.php');

// Έναρξη συνεδρίας για τον έλεγχο των δικαιωμάτων του χρήστη
session_start();

/**
 * 1. Έλεγχος Πρόσβασης και Παραμέτρων
 * Διακόπτουμε τη διαδικασία αν:
 * - Ο χρήστης δεν είναι συνδεδεμένος (user_id).
 * - Δεν έχει σταλεί το ID της δράσης προς διαγραφή μέσω της μεθόδου GET.
 */
if (!isset($_SESSION['user_id']) || !isset($_GET['id'])) {
    header("Location: actions.php");
    exit();
}

// Μετατροπή του ID σε ακέραιο για προστασία από κακόβουλα input
$action_id = (int)$_GET['id'];

/**
 * 2. ΑΣΦΑΛΕΙΑ (Authorization Check)
 * Είναι εξαιρετικά σημαντικό να μην διαγράφουμε μόνο βάσει του ID της δράσης.
 * Προσθέτουμε στο ερώτημα τον περιορισμό 'user_id = ?'.
 * Με αυτόν τον τρόπο, η διαγραφή θα εκτελεστεί ΜΟΝΟ αν η δράση ανήκει 
 * στον χρήστη που είναι αυτή τη στιγμή συνδεδεμένος.
 */
$sql = "DELETE FROM actions WHERE id = ? AND user_id = ?";

// Χρήση Prepared Statement για την αποφυγή SQL Injection
$stmt = $pdo->prepare($sql);
$stmt->execute([$action_id, $_SESSION['user_id']]);

/**
 * 3. Ολοκλήρωση και Ανακατεύθυνση
 * Αφού εκτελεστεί η εντολή, επιστρέφουμε τον χρήστη στη λίστα με τις δράσεις.
 */
header("Location: actions.php");
exit();
?>