<?php
/**
 * logout.php
 * Script για την ασφαλή τερματισμό της συνεδρίας του χρήστη.
 */

/**
 * 1. Έναρξη/Σύνδεση με το Session
 * Πριν καταστρέψουμε ένα session, πρέπει πρώτα να έχουμε πρόσβαση σε αυτό.
 * Η κλήση session_start() ανακτά την υπάρχουσα συνεδρία μέσω του Session ID.
 */
session_start();

/**
 * 2. Καθαρισμός Δεδομένων
 * Η session_unset() αφαιρεί όλες τις μεταβλητές από το $_SESSION,
 * ενώ η session_destroy() καταστρέφει οριστικά τη δομή του session στον server.
 */
session_unset();   // Αδειάζει τις μεταβλητές
session_destroy(); // Καταστρέφει το αρχείο της συνεδρίας

/**
 * 3. Ασφαλής Ανακατεύθυνση
 * Μετά την έξοδο, στέλνουμε τον χρήστη πίσω στην αρχική σελίδα.
 * Χρησιμοποιούμε την exit() αμέσως μετά για να διασφαλίσουμε ότι
 * δεν θα εκτελεστεί κανένας περαιτέρω κώδικας στο script.
 */
header("Location: index.php");
exit();
?>